          prr_d = pr_d + perc_d
          prr = pr + perc
          CALL GR_TRANSFER_D(5._sp, prcp(row, col), prr, prr_d, &
&                      parameters%opr_parameters%values(row, col, 2), &
&                      parameters_d%opr_parameters%values(row, col, 2), &
