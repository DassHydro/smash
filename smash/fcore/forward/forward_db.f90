&           local_active_cell(row, col) .EQ. 0)) THEN
!% [ END IF PRCP GAP ]
          IF (prcp(row, col) .GE. 0._sp .AND. pet(row, col) .GE. 0._sp) &
&         THEN
            IF (pet(row, col) .GT. prcp(row, col)) THEN
              ei = prcp(row, col)
            ELSE
              ei = pet(row, col)
            END IF
            IF (0._sp .LT. prcp(row, col) - ei) THEN
              pn = prcp(row, col) - ei
            ELSE
              pn = 0._sp
            END IF
            en = pet(row, col) - ei
!% =============================================================================================== %!
!%   Production module
!% =============================================================================================== %!
            en_d = 0.0_4
            pn_d = 0.0_4
            CALL GR_PRODUCTION_D(pn, pn_d, en, en_d, parameters%&
