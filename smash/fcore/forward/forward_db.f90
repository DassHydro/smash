    REAL(sp), DIMENSION(mesh%nrow, mesh%ncol) :: wa2d_b, norm_desc_b
    INTEGER :: branch
    j = 0
    DO i=1,setup%nop
      IF (options%optimize%opr_parameters(i) .NE. 1) THEN
        CALL PUSHCONTROL1B(0)
      ELSE
        CALL PUSHINTEGER4(j)
