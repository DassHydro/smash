    TYPE(PARAMETERSDT), INTENT(INOUT) :: parameters
    TYPE(PARAMETERSDT), INTENT(INOUT) :: parameters_b
    TYPE(OPTIONSDT), INTENT(IN) :: options
    INTEGER :: i, j, k
